-- DevBank Database Schema for Supabase
-- Execute this SQL in the Supabase SQL Editor

-- ==================== TABELAS ====================

-- Tabela de Cartões
CREATE TABLE IF NOT EXISTS cards (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  holder text not null,
  number text not null,
  expiry text not null,
  cvv text not null,
  brand text,
  balance numeric default 0,
  limit numeric default 0,
  color text,
  bank text,
  type text,
  created_at timestamp default now(),
  updated_at timestamp default now()
);

-- Tabela de Transações
CREATE TABLE IF NOT EXISTS transactions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  category text,
  amount numeric not null,
  date date not null,
  icon text,
  color text,
  type text not null,
  card_id bigint references cards(id) on delete set null,
  created_at timestamp default now(),
  updated_at timestamp default now()
);

-- Tabela de Bancos
CREATE TABLE IF NOT EXISTS banks (
  id bigint generated by default as identity primary key,
  name text not null,
  sub text,
  color text,
  logo text,
  connected boolean default false,
  created_at timestamp default now(),
  updated_at timestamp default now()
);

-- Tabela de Chaves PIX
CREATE TABLE IF NOT EXISTS pix_keys (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  key_type text not null,
  key_value text not null,
  is_primary boolean default false,
  created_at timestamp default now(),
  updated_at timestamp default now(),
  unique(user_id, key_type, key_value)
);

-- ==================== ÍNDICES ====================

CREATE INDEX IF NOT EXISTS cards_user_id_idx ON cards(user_id);
CREATE INDEX IF NOT EXISTS transactions_user_id_idx ON transactions(user_id);
CREATE INDEX IF NOT EXISTS transactions_date_idx ON transactions(date);
CREATE INDEX IF NOT EXISTS transactions_category_idx ON transactions(category);
CREATE INDEX IF NOT EXISTS transactions_type_idx ON transactions(type);
CREATE INDEX IF NOT EXISTS pix_keys_user_id_idx ON pix_keys(user_id);

-- ==================== RLS (Row Level Security) ====================

-- Habilitar RLS em todas as tabelas
ALTER TABLE cards ENABLE ROW LEVEL SECURITY;
ALTER TABLE transactions ENABLE ROW LEVEL SECURITY;
ALTER TABLE banks ENABLE ROW LEVEL SECURITY;
ALTER TABLE pix_keys ENABLE ROW LEVEL SECURITY;

-- ==================== POLÍTICAS - CARDS ====================

-- Usuários podem ver apenas seus próprios cartões
CREATE POLICY "Users can view own cards" ON cards
  FOR SELECT
  USING (auth.uid() = user_id);

-- Usuários podem inserir cartões
CREATE POLICY "Users can insert own cards" ON cards
  FOR INSERT
  WITH CHECK (auth.uid() = user_id);

-- Usuários podem atualizar seus próprios cartões
CREATE POLICY "Users can update own cards" ON cards
  FOR UPDATE
  USING (auth.uid() = user_id);

-- Usuários podem deletar seus próprios cartões
CREATE POLICY "Users can delete own cards" ON cards
  FOR DELETE
  USING (auth.uid() = user_id);

