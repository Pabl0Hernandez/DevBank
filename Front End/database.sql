-- ==================== POLÍTICAS - BANKS ====================

-- Qualquer pessoa autenticada pode ver bancos (dados públicos/compartilhados)
CREATE POLICY "Authenticated users can view banks" ON banks
  FOR SELECT
  USING (auth.role() = 'authenticated');

-- Apenas administradores podem inserir bancos (role future-proofing)
CREATE POLICY "Only admins can insert banks" ON banks
  FOR INSERT
  WITH CHECK (false);

-- Apenas administradores podem atualizar bancos
CREATE POLICY "Only admins can update banks" ON banks
  FOR UPDATE
  USING (false);

-- Apenas administradores podem deletar bancos
CREATE POLICY "Only admins can delete banks" ON banks
  FOR DELETE
  USING (false);

